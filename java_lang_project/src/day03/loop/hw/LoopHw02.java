package day03.loop.hw;

public class LoopHw02 {

    public static void main(String[] args) {

        // 과제3 (1)
        for(int i=0;i<5;i++){
            for(int j=0;j<i+1;j++){
                System.out.print("*");
            }
            System.out.println();
        }

        System.out.println();
    }
}
