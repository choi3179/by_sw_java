package day03.loop.hw;

public class LoopHw03 {
    public static void main(String[] args) {

        // 과제3 (2)
        for(int i=0;i<5;i++){
            for(int j=5;j>i;j--){
                System.out.print("*");
            }
            System.out.println();
        }

        System.out.println();
    }
}
